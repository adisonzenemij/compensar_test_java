public class mEmployee {
    private int id;
    private String document;
    private String names;
    private String srnms;
    private int age;
    private int time;
    private int benefit;
    private int working;
    
    // Getter y Setter para id
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter y Setter para document
    public String getDocument() {
        return document;
    }

    public void setDocument(String document) {
        this.document = document;
    }

    // Getter y Setter para names
    public String getNames() {
        return names;
    }

    public void setNames(String names) {
        this.names = names;
    }

    // Getter y Setter para srnms
    public String getSrnms() {
        return srnms;
    }

    public void setSrnms(String srnms) {
        this.srnms = srnms;
    }

    // Getter y Setter para age
    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    // Getter y Setter para time
    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    // Getter y Setter para benefit
    public int getBenefit() {
        return benefit;
    }

    public void setBenefit(int benefit) {
        this.benefit = benefit;
    }

    // Getter y Setter para working
    public int getWorking() {
        return working;
    }

    public void setWorking(int working) {
        this.working = working;
    }
}
